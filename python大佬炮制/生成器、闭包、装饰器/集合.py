#集合可以去重

a=[1,1,2,2,3,4,5,6,6,6,5,3,7,8,9,9,9]
b=[1,1,2,2,3,4,5,6,6,6,5,3,7,8,9,9,9]
a=set(a)   #{1, 2, 3, 4, 5, 6, 7, 8, 9}
print(a)

a=list(a)
print(a)    #[1, 2, 3, 4, 5, 6, 7, 8, 9]    完成去重了

#集合求交集，并集，差集

a='abcd'
a=set(a)
print(a)   #结果{'c', 'a', 'd', 'b'}

b='cdef'
b=set(b)

#求a,b交集
print(a&b)   #{'c', 'd'}


#求a,b并集
print(a|b)   #{'c', 'a', 'f', 'b', 'e', 'd'}


#求a,b差集

print(a-b)   #{'a', 'b'}

print(b-a)   #{'f', 'e'}